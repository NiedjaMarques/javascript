/*ESTRUTURA DE REPETIÇÃO (FOR) COM VARIAVEL DE CONTROLE*/

for (n = 1; n <= 8; n++) { 
    console.log(`passo ${n}`)    
}

/*
1 - dar um valor a n - de inicialização
2 - faz o teste lógico n é menor ou igual a 8 ? enquanto for verdade, o bloco será executado
3 - enquanto for verdadeiro, será acrescentado +1 a n, nesse caso quando chegar em 8, a repetição para de ser executada
*/